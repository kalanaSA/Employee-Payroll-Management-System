            jTextField7.setText(add6);
            String add7=rs.getString("status");
            jTextField8.setText(add7);
            String add8=rs.getString("dateHired");
            jTextField9.setText(add8);
            String add9=rs.getString("jobTitle");
            jTextField10.setText(add9);
